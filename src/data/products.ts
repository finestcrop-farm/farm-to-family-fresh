export const getProductById = (id: string): Product | undefined => {
  return products.find(p => p.id === id);
};

export const getProductsByCategory = (categoryId: string): Product[] => {
  return products.filter(p => p.category === categoryId);
};

export const getProductsBySubcategory = (categoryId: string, subcategoryId: string): Product[] => {
  return products.filter(p => p.category === categoryId && p.subcategory === subcategoryId);
};

export const getFeaturedProducts = (): Product[] => {
  return products.filter(p => p.rating && p.rating >= 4.5).slice(0, 12);
};

export const getOfferProducts = (): Product[] => {
  return products.filter(p => p.originalPrice && p.originalPrice > p.price).slice(0, 8);
};

export const getBestSellers = (): Product[] => {
  return products
    .filter(p => p.reviewCount && p.reviewCount > 200)
    .sort((a, b) => (b.reviewCount || 0) - (a.reviewCount || 0))
    .slice(0, 8);
};

export const getProductsByDietaryTag = (tag: string): Product[] => {
  return products.filter(p => p.dietaryTags?.includes(tag as any));
};

export const searchProducts = (query: string): Product[] => {
  const lowercaseQuery = query.toLowerCase();
  return products.filter(p => 
    p.name.toLowerCase().includes(lowercaseQuery) ||
    p.nameHindi?.toLowerCase().includes(lowercaseQuery) ||
    p.nameTelugu?.toLowerCase().includes(lowercaseQuery) ||
    p.description?.toLowerCase().includes(lowercaseQuery) ||
    p.category.toLowerCase().includes(lowercaseQuery) ||
    p.subcategory.toLowerCase().includes(lowercaseQuery)
  );
};
